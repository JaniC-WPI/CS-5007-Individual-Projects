def main():

    print()
    print("5 + 2 = ", 5 + 2)
    print("5 - 2 = ", 5 - 2)
    print("5 * 2 = ", 5 * 2)
    print("5 / 2 = ", 5 / 2)
    print("5 // 2 = ", 5 // 2)
    print("5 ** 2 = ", 5 ** 2)
    print("5 % 2 = ", 5 % 2)
    print("abs(-5) = ", abs(-5))
    print("int(5.5) = ", int(5.5))
    print("float(\"5.5\") = ", float("5.5"))
    print("complex(5, 1) = ", complex(5, 1))
    print("complex(5, 1).conjugate() = ", complex(5, 1).conjugate()) # The complex conjugate of a complex number is the number with an equal real part and an imaginary part equal in magnitude but opposite in sign.
    print("divmod(5, 2) = ", divmod(5, 2))

if __name__ == "__main__":
    main()